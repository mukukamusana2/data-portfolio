-- sales revenue by payment method

WITH order_revenue AS (
    SELECT
        fs.order_id,
        SUM(fs.price) AS order_revenue
    FROM FactSales AS fs
    INNER JOIN DimOrders AS o
        ON fs.order_id = o.order_id
    WHERE o.order_status = 'delivered'
    GROUP BY fs.order_id
)
SELECT
    op.payment_type,
    SUM(orv.order_revenue) AS total_revenue,
    COUNT(DISTINCT orv.order_id) AS total_orders,
    ROUND((SUM(orv.order_revenue) / COUNT(DISTINCT orv.order_id))::numeric, 2) AS average_order_value,
    RANK() OVER (ORDER BY SUM(orv.order_revenue) DESC) AS revenue_rank
FROM order_revenue AS orv
INNER JOIN DimOrderPayments AS op
    ON orv.order_id = op.order_id
GROUP BY op.payment_type
ORDER BY total_revenue DESC;
